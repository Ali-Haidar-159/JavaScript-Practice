"use strict"

//circle area calculation : 
export function circleArea(r)
{
    let area = 0 ;
    area = Math.PI * Math.pow(r,2) ;

    return area ;
}

// rectangle area calculation : 
export function rectangleArea(height,width)
{
    var area = 0 ; 
    area = height * width ;

    return area ;
}

export function squareArea(x)
{

    var area = 0 ;

    area = x * x  ;

    return area ;

}

